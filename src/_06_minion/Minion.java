package _06_minion;

public class Minion {
	private String name;
	private int eyes;
	private String color;
	private String master;

	
	
	public String getName() {
		return this.name;
	}

	public int getEyes() {
		return this.eyes;
	}

	public String getColor() {
		return this.color;
	}

	public String getMaster() {
		return this.master;
	}

}
